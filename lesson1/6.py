# int
a = 7
b = 3
print(a, type(a))
print(a + b, a - b, a * b, a ** b, a / b, a // b, a % b)
print(3 * 4 + 10 / 3 ** 2)
# float
a = 3.5
b = 2
print(a + b, type(a + b))

# str
a = '123'
b = 'qwe'
print(a, type(a))
print(a + b, b + a)
print(b * 10)

# bool
a = True
b = False

print(a or b, a and b, not a)

k = '123'
print(int(k) + 1)

i = 12
print(str(i) + 'qwe')